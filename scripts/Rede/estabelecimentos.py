@dataclass(frozen=True)
class ContaEstabelecimento:
    email: str
    senha: str
    pv: str
    subsidiary: str

@dataclass(frozen=True)
class UsuarioRede:
    email: str
    senha: str
    estabelecimentos: list[str]

    def contas(self):
        for est in self.estabelecimentos:
            est_str = str(est)
            yield ContaEstabelecimento(
                email=self.email,
                senha=self.senha,
                pv=est_str,
                subsidiary=est_str
            )

def get_usuarios():
    return [
        UsuarioRede(
            email=item["email"],
            senha=item["senha"],
            estabelecimentos=[str(x) for x in item["estabelecimentos"]]
        )
        for item in USUARIOS_RAW
    ]


